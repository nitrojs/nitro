import type { Compiler, RspackPluginInstance } from "@rspack/core";
import type { ExternalsTraceOptions } from "nf3";

import { builtinModules } from "node:module";
import { pathToFileURL } from "node:url";
import consola from "consola";
import { resolveModulePath } from "exsolve";
import { isAbsolute } from "pathe";

import { runtimeDir } from "nitro/meta";

import { toPathRegExp } from "../../../utils/regex.ts";
import { guessSubpath, resolveTraceDeps, toImport } from "../../_externals-utils.ts";

export type NitroExternalsTrace = Omit<
  ExternalsTraceOptions,
  "rootDir" | "exportConditions" | "traceOptions"
>;

export interface NitroExternalsOptions {
  rootDir: string;
  conditions: string[];
  /** Bundle these (matched against requests/resolved paths). */
  exclude: (string | RegExp)[];
  /** Force-trace these (typically `nitro.options.traceDeps`). */
  include: (string | RegExp)[];
  /** Production trace config, or `false` to keep bundles pinned to absolute file URLs (dev/prerender). */
  trace: false | NitroExternalsTrace;
}

type ExternalsCallback = (
  err?: null | Error,
  result?: string | string[] | boolean | Record<string, unknown>
) => void;

type ExternalsContext = {
  request?: string;
  context?: string;
  contextInfo?: { issuer?: string };
};

const PLUGIN_NAME = "NitroExternalsPlugin";

const NODE_BUILTINS = new Set([
  ...builtinModules,
  ...builtinModules.map((m) => `node:${m}`),
]);

/**
 * Externals + node_modules tracing for rspack — port of `src/build/plugins/externals.ts`.
 *
 * Two responsibilities, separated for rspack's pipeline:
 *
 *   - `externalsFunction()` returns the callback passed to `rspack.externals` config so each
 *     module resolution can be turned into an absolute `file://` URL (dev/prerender) or a
 *     bare specifier `pkgname/subpath` (production tracing). In tracing mode, the resolved
 *     absolute path is collected into `tracedPaths` for stage 2.
 *
 *   - `apply(compiler)` taps `compiler.hooks.afterEmit` and invokes `nf3.traceNodeModules()`
 *     with the collected paths, copying the minimal `node_modules` graph into
 *     `output.serverDir/node_modules/` so the build is portable.
 *
 * Skip tracing entirely when `trace === false` (set by callers for dev / nitro-prerender).
 */
export class NitroExternalsPlugin implements RspackPluginInstance {
  readonly opts: NitroExternalsOptions;

  private resolvedTraceDeps?: ReturnType<typeof resolveTraceDeps>;
  private include?: RegExp[];
  private exclude: RegExp[];
  private resolveConditions: string[];
  private tracedPaths = new Set<string>();

  constructor(opts: NitroExternalsOptions) {
    this.opts = opts;

    this.resolvedTraceDeps = opts.trace ? resolveTraceDeps(opts.include) : undefined;
    this.include = this.resolvedTraceDeps?.includePattern
      ? [this.resolvedTraceDeps.includePattern]
      : undefined;
    this.exclude = [
      /^(?:[\0#~.]|[a-z0-9]{2,}:)|\?/,
      ...(opts.exclude || []).map((p) => toPathRegExp(p)),
    ];

    // exsolve uses only the supplied conditions (no implicit `import`/`default`). Add `import`
    // so packages whose `exports` only declares it (e.g. lightningcss) resolve correctly.
    this.resolveConditions = opts.conditions.includes("import")
      ? opts.conditions
      : [...opts.conditions, "import"];
  }

  /** Returns the externals callback for `Configuration.externals`. */
  externalsFunction() {
    const trace = this.opts.trace;
    const include = this.include;
    const exclude = this.exclude;
    // Try resolution from the importer's directory first; fall back to the project root and
    // finally nitro's runtime dir so virtual modules generated by nitro (which import deps
    // like `h3`, `hookable`) resolve correctly when their on-disk location lives outside the
    // project's node_modules tree (e.g. our in-memory VFS root).
    const tryResolve = (id: string, from: string | undefined): string | undefined => {
      const candidates = [
        from && isAbsolute(from) ? from : undefined,
        this.opts.rootDir,
        runtimeDir,
      ].filter(Boolean) as string[];
      for (const candidate of candidates) {
        const resolved = resolveModulePath(id, {
          try: true,
          from: candidate,
          conditions: this.resolveConditions,
        });
        if (resolved) return resolved;
      }
      return undefined;
    };

    // Tracing mode is opt-in per options.trace; when enabled but no patterns match anything,
    // every request falls through to default rspack handling (matching rolldown behavior).
    const tracingEnabled = !!trace && !!this.resolvedTraceDeps?.includePattern;

    const filter = (id: string) => {
      if (include && !include.some((r) => r.test(id))) return false;
      if (exclude.some((r) => r.test(id))) return false;
      return true;
    };

    return (data: ExternalsContext, callback: ExternalsCallback) => {
      const request = data.request;
      if (!request) return callback();

      // Always externalize Node built-ins with a normalized `node:` prefix.
      if (NODE_BUILTINS.has(request) || request.startsWith("node:")) {
        const target = request.startsWith("node:") ? request : `node:${request}`;
        return callback(null, `module-import ${target}`);
      }

      // Local/relative imports — let rspack handle.
      if (request.startsWith(".") || isAbsolute(request)) return callback();

      // Pre-filter by request before paying the resolution cost.
      if (!filter(request)) return callback();

      // Resolve to an absolute filesystem path so we can match against include/exclude
      // patterns and feed nf3.
      const resolved = tryResolve(request, data.context);
      if (!resolved) return callback();
      if (!filter(resolved)) return callback();

      if (tracingEnabled) {
        let importId = toImport(request) || toImport(resolved);
        if (!importId) return callback();
        // Verify the bare specifier resolves — if not, fall back to a guessed subpath
        // from the package's exports map.
        if (!tryResolve(importId, data.context)) {
          const guessed = guessSubpath(resolved, this.resolveConditions);
          if (!guessed) return callback();
          importId = guessed;
        }
        this.tracedPaths.add(resolved);
        return callback(null, `module-import ${importId}`);
      }

      // No tracing — emit an absolute `file://` URL. The build is non-portable but works for
      // dev / prerender where the build machine is the runtime.
      return callback(null, `module-import ${pathToFileURL(resolved).href}`);
    };
  }

  apply(compiler: Compiler): void {
    if (!this.opts.trace) return;

    compiler.hooks.afterEmit.tapPromise(PLUGIN_NAME, async () => {
      if (this.tracedPaths.size === 0) return;
      const trace = this.opts.trace as NitroExternalsTrace;
      const { hooks: userHooks, ...traceOpts } = trace;
      const { traceNodeModules } = await import("nf3");
      const traceTime = Date.now();
      let traceFilesCount = 0;
      let tracedPkgsCount = 0;
      await traceNodeModules([...this.tracedPaths], {
        ...traceOpts,
        fullTraceInclude: this.resolvedTraceDeps?.fullTraceInclude,
        conditions: this.opts.conditions,
        rootDir: this.opts.rootDir,
        writePackageJson: true, // deno compat
        hooks: {
          ...userHooks,
          tracedFiles: async (result) => {
            traceFilesCount = Object.keys(result).length;
            await userHooks?.tracedFiles?.(result);
          },
          tracedPackages: async (pkgs) => {
            tracedPkgsCount = Object.keys(pkgs).length;
            consola.info(
              `Tracing dependencies:\n${Object.entries(pkgs)
                .map(
                  ([name, versions]) =>
                    `- \`${name}\` (${Object.keys(versions.versions).join(", ")})`
                )
                .join("\n")}`
            );
            await userHooks?.tracedPackages?.(pkgs);
          },
        },
      });
      consola.success(
        `Traced ${tracedPkgsCount} dependencies (${traceFilesCount} files) in ${Date.now() - traceTime}ms.`
      );
      consola.info(
        `Ensure your production environment matches the builder OS and architecture (\`${process.platform}-${process.arch}\`) to avoid native module issues.`
      );
    });
  }
}
